# Create variables with different data types and use type() to display their types. Create: a number (int/float), a string, a boolean (True/False), and display each value with its type.

data_type_int = 42
data_type_string = "Hello"
data_type_boolean = True

print(data_type_int, type(data_type_int))
print(data_type_string, type(data_type_string))
print(data_type_boolean, type(data_type_boolean))

# Hint: Use print(variable, type(variable)) to show both value and type

# Expected Output (example)
# 42 <class 'int'>
# "Hello" <class 'str'>
# True <class 'bool'>

